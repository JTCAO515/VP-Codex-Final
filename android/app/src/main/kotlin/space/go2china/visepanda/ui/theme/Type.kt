import androidx.compose.ui.unit.sp

/**
 * v0.3.3 uses system font families only (default serif for headlines, the
 * platform sans for body text) rather than bundling a custom serif like the
 * web app's Georgia headline font — pulling in a licensed/Google Font adds
 * APK size and a licensing check that is out of scope for the foundation
 * round. Swapping FontFamily.Serif for a bundled font later does not change
 * any call site, since screens reference `MaterialTheme.typography.*`, not
 * this file directly.
 */
